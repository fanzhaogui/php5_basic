## PECL

    一个从PEAR脱离的项目是PECL，英文全称PHP Extension Community Library.
PECL 曾经是PEAR的一个子项目，但是今天，它是单独管理的。这意味着PECL有它自己的Web站点、邮件列表、程序管理等等。

    但是，PEAR和PECL共享工具和架构：他们都使用PEAR安装程序，都使用相同的数据包格式，并且都使用相同的版本标准。

    不过编码标准是不一样的：PECL遵循的是PHP编码标准（对于C代码），而PEAR使用它自身的标准

